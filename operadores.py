#teoria: operadores logicos

#mayor estricto ">"
print(3 > 2) #true

#menor estricto "<"
print(4 < 2) #false

#mayor o igual ">="
print(3 >= 3) #true

#menor o igual "<="
print(4 <= 1) #false

#es igual
print(2 == 2) #true

#diferente
print(3 != 2) #true

#comparacion
print("operacion", 1 < 2 and 3 != 2) #true

#Rango
print(3 < 4 > 2) #true
